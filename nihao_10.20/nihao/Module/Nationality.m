#import "Nationality.h"

@implementation Nationality

- (id)copyWithZone:(NSZone *)zone {
    Nationality *nation = [[Nationality allocWithZone:zone] init];
    nation.country_name_en = _country_name_en;
    nation.country_id = _country_id;
    return nation;
}

@end
